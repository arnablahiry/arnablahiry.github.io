#!/usr/bin/env node

const fs = require('fs');
const path = require('path');
const vm = require('vm');
const XLSX = require('xlsx');

const repoRoot = path.resolve(__dirname, '..');
const manifestPath = path.join(repoRoot, 'scripts', 'mywildindia-photo-manifest.js');
const captionJsPath = path.join(repoRoot, 'scripts', 'mywildindia-captions.js');
const workbookPath = path.join(repoRoot, 'data', 'mywildindia-captions.xlsx');

function loadWindowKey(filePath, key) {
  if (!fs.existsSync(filePath)) return null;
  const code = fs.readFileSync(filePath, 'utf8');
  const sandbox = { window: {} };
  vm.runInNewContext(code, sandbox, { filename: filePath });
  return sandbox.window[key] || null;
}

function sanitizeSheetName(value) {
  const cleaned = String(value || 'Sheet')
    .replace(/[\\/?*\[\]:]/g, ' ')
    .replace(/\s+/g, ' ')
    .trim();
  return (cleaned || 'Sheet').slice(0, 31);
}

function readWorkbookCaptionsByFolder(workbook, folderNames) {
  const result = {};

  folderNames.forEach((folder) => {
    const sheetName = sanitizeSheetName(folder);
    const ws = workbook.Sheets[sheetName];
    if (!ws) return;

    const rows = XLSX.utils.sheet_to_json(ws, { defval: '' });
    const folderCaptions = {};

    rows.forEach((row) => {
      const photo = (row.Photo || row.photo || '').toString().trim();
      const caption = (row.Caption || row.caption || '').toString().trim();
      const author = (row.Author || row.author || '').toString().trim();

      if (photo) {
        folderCaptions[photo] = {
          caption: caption || null,
          author: author || null
        };
      }
    });

    result[folder] = folderCaptions;
  });

  return result;
}

const manifest = loadWindowKey(manifestPath, 'MYWILDINDIA_PHOTO_MANIFEST');
if (!manifest || typeof manifest !== 'object') {
  throw new Error(`Unable to read manifest from ${manifestPath}. Run generate-mywildindia-photo-manifest first.`);
}

const folderNames = Object.keys(manifest).sort();
const captionSeed = loadWindowKey(captionJsPath, 'MYWILDINDIA_CAPTIONS') || {};
let workbookCaptionSeed = {};

if (fs.existsSync(workbookPath)) {
  const existingWorkbook = XLSX.readFile(workbookPath);
  workbookCaptionSeed = readWorkbookCaptionsByFolder(existingWorkbook, folderNames);
}

const nextCaptionMap = {};
const wb = XLSX.utils.book_new();

folderNames.forEach((folder) => {
  const photos = Array.isArray(manifest[folder]) ? manifest[folder] : [];
  const existingCaptions = workbookCaptionSeed[folder] || {};
  const seedCaptions = captionSeed[folder] || {};

  const sheetRows = [['Photo', 'Caption', 'Author']];
  const finalCaptions = {};

  photos.forEach((photoEntry) => {
    const src = photoEntry.src || '';
    const fileName = src ? src.split('/').pop() : '';
    const existingCaption = existingCaptions[fileName] || seedCaptions[fileName];
    const caption = (existingCaption && existingCaption.caption) || '';
    const author = (existingCaption && existingCaption.author) || '';

    sheetRows.push([fileName, caption || '', author || '']);
    finalCaptions[fileName] = { caption: caption || null, author: author || null };
  });

  nextCaptionMap[folder] = finalCaptions;

  const ws = XLSX.utils.aoa_to_sheet(sheetRows);
  ws['!cols'] = [{ wch: 40 }, { wch: 80 }, { wch: 16 }];
  XLSX.utils.book_append_sheet(wb, ws, sanitizeSheetName(folder));
});

fs.mkdirSync(path.dirname(workbookPath), { recursive: true });
XLSX.writeFile(wb, workbookPath);

const jsBody = [
  '/* Auto-generated by scripts/sync-mywildindia-caption-spreadsheet.js.',
  '   Edit data/mywildindia-captions.xlsx and rerun the script. */',
  '(function () {',
  `  window.MYWILDINDIA_CAPTIONS = ${JSON.stringify(nextCaptionMap, null, 2).replace(/\n/g, '\n  ')};`,
  '})();',
  ''
].join('\n');

fs.writeFileSync(captionJsPath, jsBody, 'utf8');

const photoCount = folderNames.reduce((sum, folder) => sum + (Array.isArray(manifest[folder]) ? manifest[folder].length : 0), 0);
console.log(`Synced ${path.relative(repoRoot, workbookPath)} and ${path.relative(repoRoot, captionJsPath)} (${folderNames.length} folders, ${photoCount} photos).`);
