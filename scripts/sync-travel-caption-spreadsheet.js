#!/usr/bin/env node

const fs = require('fs');
const path = require('path');
const vm = require('vm');
const XLSX = require('xlsx');

const repoRoot = path.resolve(__dirname, '..');
const manifestPath = path.join(repoRoot, 'scripts', 'travel-photo-manifest.js');
const captionJsPath = path.join(repoRoot, 'scripts', 'travel-captions.js');
const workbookPath = path.join(repoRoot, 'data', 'travel-captions.xlsx');

function loadWindowKey(filePath, key) {
  if (!fs.existsSync(filePath)) return null;
  const code = fs.readFileSync(filePath, 'utf8');
  const sandbox = { window: {} };
  vm.runInNewContext(code, sandbox, { filename: filePath });
  return sandbox.window[key] || null;
}

function titleFromSlug(value) {
  return String(value || '')
    .replace(/[-_]+/g, ' ')
    .replace(/\s+/g, ' ')
    .trim()
    .replace(/\b\w/g, (letter) => letter.toUpperCase());
}

function sanitizeSheetName(value) {
  const cleaned = String(value || 'Sheet')
    .replace(/[\\/?*\[\]:]/g, ' ')
    .replace(/\s+/g, ' ')
    .trim();
  return (cleaned || 'Sheet').slice(0, 31);
}

function sheetNameForSlug(slug, usedNames) {
  const baseName = sanitizeSheetName(titleFromSlug(slug));
  let candidate = baseName;
  let index = 2;

  while (usedNames.has(candidate.toLowerCase())) {
    const suffix = ` ${index}`;
    const maxLen = Math.max(1, 31 - suffix.length);
    candidate = `${baseName.slice(0, maxLen)}${suffix}`;
    index += 1;
  }

  usedNames.add(candidate.toLowerCase());
  return candidate;
}

function readWorkbookCaptionsBySlug(workbook, slugToSheetName) {
  const result = {};

  Object.entries(slugToSheetName).forEach(([slug, sheetName]) => {
    const ws = workbook.Sheets[sheetName];
    if (!ws) return;
    const rows = XLSX.utils.sheet_to_json(ws, { header: 1, raw: false });
    const captions = [];

    for (let i = 1; i < rows.length; i += 1) {
      const value = rows[i] && rows[i][1];
      captions.push(value == null ? '' : String(value));
    }

    result[slug] = captions;
  });

  return result;
}

const manifest = loadWindowKey(manifestPath, 'TRAVEL_PHOTO_MANIFEST');
if (!manifest || typeof manifest !== 'object') {
  throw new Error(`Unable to read manifest from ${manifestPath}. Run generate-travel-photo-manifest first.`);
}

const sortedSlugs = Object.keys(manifest).sort((a, b) => a.localeCompare(b, undefined, { numeric: true, sensitivity: 'base' }));
const slugToSheetName = {};
const usedSheetNames = new Set();
sortedSlugs.forEach((slug) => {
  slugToSheetName[slug] = sheetNameForSlug(slug, usedSheetNames);
});

const captionSeed = loadWindowKey(captionJsPath, 'TRAVEL_CAPTIONS') || {};
let workbookCaptionSeed = {};
if (fs.existsSync(workbookPath)) {
  const existingWorkbook = XLSX.readFile(workbookPath);
  workbookCaptionSeed = readWorkbookCaptionsBySlug(existingWorkbook, slugToSheetName);
}

const nextCaptionMap = {};
const wb = XLSX.utils.book_new();

sortedSlugs.forEach((slug) => {
  const photos = Array.isArray(manifest[slug]) ? manifest[slug] : [];
  const existingCaptions = Array.isArray(workbookCaptionSeed[slug])
    ? workbookCaptionSeed[slug]
    : (Array.isArray(captionSeed[slug]) ? captionSeed[slug] : []);

  const sheetRows = [['Photo', 'Caption']];
  const finalCaptions = [];

  photos.forEach((_, index) => {
    const caption = typeof existingCaptions[index] === 'string' ? existingCaptions[index] : '';
    sheetRows.push([`photo ${index + 1}`, caption]);
    finalCaptions.push(caption);
  });

  nextCaptionMap[slug] = finalCaptions;

  const ws = XLSX.utils.aoa_to_sheet(sheetRows);
  ws['!cols'] = [{ wch: 14 }, { wch: 90 }];
  XLSX.utils.book_append_sheet(wb, ws, slugToSheetName[slug]);
});

fs.mkdirSync(path.dirname(workbookPath), { recursive: true });
XLSX.writeFile(wb, workbookPath);

const jsBody = [
  '/* Auto-generated by scripts/sync-travel-caption-spreadsheet.js.',
  '   Edit data/travel-captions.xlsx and rerun the script. */',
  '(function () {',
  `  window.TRAVEL_CAPTIONS = ${JSON.stringify(nextCaptionMap, null, 2).replace(/\n/g, '\n  ')};`,
  '})();',
  ''
].join('\n');

fs.writeFileSync(captionJsPath, jsBody, 'utf8');

const photoCount = sortedSlugs.reduce((sum, slug) => sum + (Array.isArray(manifest[slug]) ? manifest[slug].length : 0), 0);
console.log(`Synced ${path.relative(repoRoot, workbookPath)} and ${path.relative(repoRoot, captionJsPath)} (${sortedSlugs.length} countries, ${photoCount} photos).`);
