#!/usr/bin/env node

const fs = require('fs');
const path = require('path');
const childProcess = require('child_process');

const repoRoot = path.resolve(__dirname, '..');
const travelRoot = path.join(repoRoot, 'images', 'travels');
const outputPath = path.join(repoRoot, 'scripts', 'travel-photo-manifest.js');
const imageExtensions = new Set(['.avif', '.gif', '.jpeg', '.jpg', '.png', '.webp']);
const thumbnailFolderName = 'thumbs';
const thumbnailMaxSize = 480;

function naturalCompare(a, b) {
  return a.localeCompare(b, undefined, { numeric: true, sensitivity: 'base' });
}

function toWebPath(filePath) {
  return path.relative(repoRoot, filePath).split(path.sep).join('/');
}

function thumbnailPathFor(filePath) {
  const parsed = path.parse(filePath);
  return path.join(parsed.dir, thumbnailFolderName, `${parsed.name}.jpg`);
}

function ensureThumbnail(sourcePath) {
  const thumbPath = thumbnailPathFor(sourcePath);
  const sourceStat = fs.statSync(sourcePath);

  if (fs.existsSync(thumbPath)) {
    const thumbStat = fs.statSync(thumbPath);
    if (thumbStat.mtimeMs >= sourceStat.mtimeMs) return thumbPath;
  }

  fs.mkdirSync(path.dirname(thumbPath), { recursive: true });
  childProcess.execFileSync('sips', [
    '-s', 'format', 'jpeg',
    '-s', 'formatOptions', '65',
    '-Z', String(thumbnailMaxSize),
    sourcePath,
    '--out', thumbPath
  ], { stdio: 'ignore' });

  return thumbPath;
}

function readManifest() {
  if (!fs.existsSync(travelRoot)) {
    throw new Error(`Travel image folder not found: ${travelRoot}`);
  }

  const manifest = {};
  const folders = fs.readdirSync(travelRoot, { withFileTypes: true })
    .filter((entry) => entry.isDirectory() && !entry.name.startsWith('.') && entry.name !== thumbnailFolderName)
    .map((entry) => entry.name)
    .sort(naturalCompare);

  folders.forEach((folder) => {
    const folderPath = path.join(travelRoot, folder);
    const files = fs.readdirSync(folderPath, { withFileTypes: true })
      .filter((entry) => entry.isFile() && !entry.name.startsWith('.'))
      .filter((entry) => imageExtensions.has(path.extname(entry.name).toLowerCase()))
      .map((entry) => path.join(folderPath, entry.name))
      .sort(naturalCompare)
      .map((sourcePath) => ({
        src: toWebPath(sourcePath),
        thumbSrc: toWebPath(ensureThumbnail(sourcePath))
      }));

    manifest[folder] = files;
  });

  return manifest;
}

function writeManifest(manifest) {
  const contents = [
    '/* Auto-generated by scripts/generate-travel-photo-manifest.js.',
    '   Drop photos into images/travels/<country-folder>/ and rerun the script. */',
    '(function () {',
    `  window.TRAVEL_PHOTO_MANIFEST = ${JSON.stringify(manifest, null, 2).replace(/\n/g, '\n  ')};`,
    '})();',
    ''
  ].join('\n');

  fs.mkdirSync(path.dirname(outputPath), { recursive: true });
  fs.writeFileSync(outputPath, contents, 'utf8');
}

const manifest = readManifest();
writeManifest(manifest);

try {
  const captionSyncScript = path.join(__dirname, 'sync-travel-caption-spreadsheet.js');
  if (fs.existsSync(captionSyncScript)) {
    childProcess.execFileSync(process.execPath, [captionSyncScript], { stdio: 'inherit' });
  }
} catch (error) {
  const reason = (error && error.message) ? error.message : String(error);
  console.warn(`Skipped travel caption spreadsheet sync: ${reason}`);
}

const count = Object.values(manifest).reduce((sum, files) => sum + files.length, 0);
console.log(`Wrote ${toWebPath(outputPath)} with ${count} photos and thumbnails across ${Object.keys(manifest).length} folders.`);
